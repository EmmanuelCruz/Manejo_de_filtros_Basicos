    /**
     * Aplica el filtro RGB a la imagen cargada en un procesador,
     * por cada vez que se cambie el valor de un slider.
     */
    public void applyRGB(){
        if(!proc.tieneImagen())
            return;
        int R, G, B;
        R=jSlider1.getValue();
        G=jSlider2.getValue();
        B=jSlider3.getValue();
        jLabel1.setIcon(new ImageIcon(proc.RGBFilter(R,G,B)));
    }
    
