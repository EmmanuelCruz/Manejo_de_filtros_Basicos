    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if(!proc.tieneImagen())
            return;
        String leido=jTextField1.getText();
        int factor=0;
        try{
            factor=Integer.parseInt(leido);
        } catch(Exception e){
            factor=0;
        }
        jLabel1.setIcon(new ImageIcon(proc.mosaico(factor)));
    }//GEN-LAST:event_jButton4ActionPerformed

