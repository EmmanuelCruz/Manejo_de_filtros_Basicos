    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        if(!proc.tieneImagen())
            return;
        int op=jComboBox1.getSelectedIndex();
        switch(op){
            case 0:
                jLabel1.setIcon(new ImageIcon(proc.escalaGrises3(op)));
                break;
            case 1:
                jLabel1.setIcon(new ImageIcon(proc.escalaGrises3(op)));
                break;
            case 2:
                jLabel1.setIcon(new ImageIcon(proc.escalaGrises3(op)));
                break;
            case 3:
                jLabel1.setIcon(new ImageIcon(proc.escalaGrises1()));
                break;
            case 4:
                jLabel1.setIcon(new ImageIcon(proc.escalaGrises2()));
                break;
        }
    }//GEN-LAST:event_jComboBox1ActionPerformed

